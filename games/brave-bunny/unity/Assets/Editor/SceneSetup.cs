    [MenuItem("Brave/Scaffold Phase-5 Scenes")]
    public static void Scaffold()
    {
        Directory.CreateDirectory(SceneDir);

        EnsureBoot();
        EnsureMainMenu();
        EnsureLoadout();
        EnsureRun();
        EnsurePerfStress();
        RegisterInBuildSettings();

        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();
        Debug.Log("[SceneSetup] OK — Boot/Run/PerfStress scenes scaffolded and registered.");
    }

    /// <summary>
    /// Crash-fix (#1) entry — deletes pre-existing empty scaffold scenes
    /// (Boot, MainMenu, Loadout, PerfStress) BEFORE running the regular Scaffold flow,
    /// so the EnsureX methods rebuild them from scratch with their full GameObject set.
    /// Run.unity is preserved (it has real authored content — 24 YAML docs, full gameplay wiring).
    /// </summary>
    [MenuItem("Brave/Force-Scaffold Phase-5 Scenes (deletes Boot/MainMenu/Loadout/PerfStress)")]
    public static void ForceScaffold()
    {
        Directory.CreateDirectory(SceneDir);

        // Delete the empty placeholder scenes (and their .meta) so EnsureX runs fresh.
        // Run.unity is NEVER deleted — it has 24 YAML docs of real authored content.
        foreach (var sceneName in new[] { "Boot", "MainMenu", "Loadout", "PerfStress" })
        {
            var unity = $"{SceneDir}/{sceneName}.unity";
            var meta = $"{unity}.meta";
            if (File.Exists(unity))
            {
                File.Delete(unity);
                Debug.Log($"[SceneSetup] ForceScaffold deleted {unity}");
            }
            if (File.Exists(meta))
            {
                File.Delete(meta);
                Debug.Log($"[SceneSetup] ForceScaffold deleted {meta}");
            }
        }

        // Refresh so AssetDatabase forgets the old GUIDs before we recreate.
        AssetDatabase.Refresh();

        Scaffold();
        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();
        Debug.Log("[SceneSetup] ForceScaffold OK — scenes deleted and rebuilt with fresh GUIDs.");
    }

    /// <summary>
    /// Wave 6 (make-it-playable) entry — backs up and deletes Run.unity so EnsureRun()
    /// regenerates it with the full gameplay loop wired (WaveSpawner, AutoAttackController,
    /// CarrotProjectilePool, RunTimer, RunController, RunHudController).
    /// Leaves Boot/MainMenu/Loadout/PerfStress intact.
    /// </summary>
    [MenuItem("Brave/Force-Scaffold Run Scene (playable — backs up Run.unity first)")]
    public static void ForceScaffoldRun()
    {
        Directory.CreateDirectory(SceneDir);

        var runUnity = $"{SceneDir}/Run.unity";
        var runMeta  = $"{runUnity}.meta";

        // Backup Run.unity to /tmp before deletion.
        if (File.Exists(runUnity))
        {
            var ts = DateTimeOffset.UtcNow.ToUnixTimeSeconds();
            var backup = $"/tmp/run-unity-before-{ts}.unity";
            File.Copy(runUnity, backup, overwrite: true);
            Debug.Log($"[SceneSetup] ForceScaffoldRun backed up {runUnity} → {backup}");
            File.Delete(runUnity);
        }
        if (File.Exists(runMeta))
            File.Delete(runMeta);

        AssetDatabase.Refresh();

        // Re-run just the Run scene (all others unchanged).
        EnsureRun();
        RegisterInBuildSettings();

        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();
        Debug.Log("[SceneSetup] ForceScaffoldRun OK — Run.unity rebuilt with gameplay loop.");
    }

