    /// <summary>
    /// Adds AutoAttackController to the Player GameObject and wires:
    ///   - player = PlayerMover on Player
    ///   - weapon = Weapon_carrot-boomerang.asset (fallback: Weapon_pebble-sling.asset)
    ///   - projectilePool = the [CarrotProjectilePool] component
    /// </summary>
    private static void EnsureRunAutoAttack(GameObject player, GameObject projectilePoolGO)
    {
        var aaType = FindType("Brave.Gameplay.Combat.AutoAttackController");
        if (aaType == null)
        {
            Debug.LogWarning("[SceneSetup] AutoAttackController type not found — skipping.");
            return;
        }

        var controller = player.GetComponent(aaType) ?? player.AddComponent(aaType);
        var so = new SerializedObject(controller);

        // Wire the PlayerMover reference.
        var mover = player.GetComponent<PlayerMover>();
        var playerProp = so.FindProperty("player");
        if (playerProp != null && mover != null)
            playerProp.objectReferenceValue = mover;

        // Resolve weapon SO — prefer carrot-boomerang (vertical slice); fall back to pebble-sling.
        WeaponDefinition? weaponDef =
            AssetDatabase.LoadAssetAtPath<WeaponDefinition>(WeaponCarrotAssetPath)
            ?? AssetDatabase.LoadAssetAtPath<WeaponDefinition>(WeaponPebbleAssetPath);
        if (weaponDef == null)
        {
            Debug.LogWarning(
                "[SceneSetup] No weapon SO found at carrot-boomerang or pebble-sling paths. "
                + "Run 'Brave > Generate Balance SOs from JSON' then re-run this scaffold. "
                + "AutoAttackController will disable its direct-cast path at runtime.");
        }
        var weaponProp = so.FindProperty("weapon");
        if (weaponProp != null)
            weaponProp.objectReferenceValue = weaponDef;

        // Wire the CarrotProjectilePool component.
        var poolType = FindType("Brave.Gameplay.Combat.CarrotProjectilePool");
        if (poolType != null && projectilePoolGO != null)
        {
            var poolComp = projectilePoolGO.GetComponent(poolType);
            var poolProp = so.FindProperty("projectilePool");
            if (poolProp != null && poolComp != null)
                poolProp.objectReferenceValue = poolComp;
        }

        so.ApplyModifiedPropertiesWithoutUndo();
        Debug.Log("[SceneSetup] AutoAttackController wired on Player.");
    }

