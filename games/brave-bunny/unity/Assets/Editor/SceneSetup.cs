    /// <summary>
    /// Creates [HUD] GameObject with:
    ///   - UIDocument component with RunHud.uxml panel-settings-asset assigned
    ///   - RunHudController component (which requires UIDocument via [RequireComponent])
    /// The HUD will render placeholder stub values via RunHudStubRuntime until
    /// IRunRuntimeState is wired by gameplay-engineer in a future dispatch.
    /// </summary>
    private static void EnsureRunHud()
    {
        var hudType = FindType("Brave.UI.Controllers.RunHudController");
        if (hudType == null)
        {
            Debug.LogWarning("[SceneSetup] RunHudController type not found — skipping HUD.");
            return;
        }

        var go = new GameObject("[HUD]");

        // UIDocument is required by RunHudController ([RequireComponent]).
        // AddComponent order matters: UIDocument first, then RunHudController.
        var uiDocType = FindType("UnityEngine.UIElements.UIDocument");
        Component? uiDoc = null;
        if (uiDocType != null)
        {
            uiDoc = go.AddComponent(uiDocType);
            // Assign the RunHud.uxml VisualTreeAsset to the UIDocument.
            var vtAsset = AssetDatabase.LoadAssetAtPath<VisualTreeAsset>(RunHudUxmlPath);
            if (vtAsset == null)
            {
                Debug.LogWarning(
                    $"[SceneSetup] RunHud.uxml not found at {RunHudUxmlPath} — "
                    + "UIDocument will have no panel content until the UXML is assigned manually.");
            }
            else
            {
                var docSo = new SerializedObject(uiDoc);
                var vtProp = docSo.FindProperty("m_VisualTreeAsset");
                if (vtProp != null)
                {
                    vtProp.objectReferenceValue = vtAsset;
                    docSo.ApplyModifiedPropertiesWithoutUndo();
                }
            }
        }
        else
        {
            Debug.LogWarning("[SceneSetup] UIDocument type not found — HUD may not render.");
        }

        go.AddComponent(hudType);
        Debug.Log("[SceneSetup] [HUD] / RunHudController wired.");
    }

    /// <summary>
    /// Adds the canonical <see cref="PlayerMover"/> to the Run-scene Player GameObject
    /// and assigns its <c>character</c> field to the Bunny CharacterDefinition SO.
    /// Idempotent — re-running the scaffold does not stack duplicate components.
    /// Assumes <c>Brave > Generate Balance SOs from JSON</c> has already produced the SO;
    /// if it hasn't, the asset lookup logs a warning and the component is added
    /// un-configured (PlayerMover.Awake will then disable itself with a clear error).
    /// </summary>
    private static void EnsureRunPlayerWiring(GameObject player)
    {
        var mover = player.GetComponent<PlayerMover>() ?? player.AddComponent<PlayerMover>();

        var charDef = AssetDatabase.LoadAssetAtPath<CharacterDefinition>(CharBunnyAssetPath);
        if (charDef == null)
        {
            Debug.LogWarning(
                $"[SceneSetup] {CharBunnyAssetPath} not found — run 'Brave > Generate Balance SOs from JSON' "
                + "before launching Play mode so PlayerMover can resolve its move-speed.");
            return;
        }

        // Use SerializedObject so the private [SerializeField] survives scene-save without
        // requiring a public setter on PlayerMover (which would loosen its API contract).
        var so = new SerializedObject(mover);
        var prop = so.FindProperty("character");
        if (prop != null)
        {
            prop.objectReferenceValue = charDef;
            so.ApplyModifiedPropertiesWithoutUndo();
        }
        else
        {
            Debug.LogWarning("[SceneSetup] PlayerMover.character SerializedProperty not found — "
                + "field may have been renamed; update CharBunnyAssetPath wiring accordingly.");
        }
    }

