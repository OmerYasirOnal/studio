    /// <summary>
    /// Creates [WaveSpawner] GameObject with a WaveSpawner component. Wires:
    ///   - hero = Player transform
    ///   - runTimer = RunTimer component from <paramref name="runTimerGO"/>
    ///   - wave = null (no WaveDefinition SO exists yet — logged warning)
    /// </summary>
    private static void EnsureRunWaveSpawner(GameObject player, GameObject runTimerGO)
    {
        var spawnerType = FindType("Brave.Gameplay.Spawning.WaveSpawner");
        if (spawnerType == null)
        {
            Debug.LogWarning("[SceneSetup] WaveSpawner type not found — skipping.");
            return;
        }

        var go = new GameObject("[WaveSpawner]");
        var comp = go.AddComponent(spawnerType);
        var so = new SerializedObject(comp);

        // hero — the Player's Transform.
        var heroProp = so.FindProperty("hero");
        if (heroProp != null)
            heroProp.objectReferenceValue = player.transform;

        // runTimer — the RunTimer component on [RunTimer] GO.
        var timerType = FindType("Brave.Gameplay.Run.RunTimer");
        if (timerType != null && runTimerGO != null)
        {
            var timerComp = runTimerGO.GetComponent(timerType);
            var timerProp = so.FindProperty("runTimer");
            if (timerProp != null && timerComp != null)
                timerProp.objectReferenceValue = timerComp;
        }

        // wave — no WaveDefinition asset exists until level-designer generates them.
        var waveProp = so.FindProperty("wave");
        if (waveProp != null)
            waveProp.objectReferenceValue = null;   // graceful null — logged below

        Debug.LogWarning(
            "[SceneSetup] WaveSpawner.wave is null — no WaveDefinition SO found. "
            + "Generate Assets/_Brave/Data/Balance/Wave_meadow.asset from "
            + "docs/09-level-design/01-biomes/meadow/waves.json to enable spawning.");

        so.ApplyModifiedPropertiesWithoutUndo();
        Debug.Log("[SceneSetup] [WaveSpawner] wired (wave=null, hero+runTimer set).");
    }

